歌头（1）

夜静更深，
月明星亮，
我们二人走进歌场。
孝家请开个歌头，
亲友都在捧场，
只因才疏学浅，
呆曾开口汗先淌。

不开天地水府，
不开日月三光，
不开五方土地，
不开闪电娘娘，
不开历代君王，
不开青狮白象，
不开虎豹豺狼，
不开魑魅魍魉，
单开黑暗混沌，
歌师可记周详？

不唱古往来今，
不唱地理天文，
不唱日月星辰，
不唱五岳山名，
不唱文臣武将，
不唱贤帝圣君，
不唱走兽飞禽，
不唱礼乐道经，
单唱黑暗混沌，
歌师可知根痕？

