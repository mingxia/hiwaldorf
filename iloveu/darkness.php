一声响亮落地平。
又见一道红气升，
空中结起五彩云。
五道光华空中现，
一尊黄石在眼前。
黄石高来有九丈，
一十二丈为周园。

一人来到石上坐，
一阵清风便来临，
哈气黄石来变化，
化成九色莲台形。
此人坐在莲台上，
心中欢喜有十分。
自己细细来思想，
要给自己取姓名。
两山之间有二孔，
内藏玄黄二字文。
一个玄字就为姓，
一个黄字就为名。

青龙山上白光起，
昆仑山上黄光生，
结成园物空中现，
落在昆仑山中存，
随风一吹成人形。
身子长来有九丈，
膀园五周有余零，
面色发黑遍身黄，
眉目清秀圣人形，
玄黄老祖是他名，
坐在莲台苦修行。

玄黄正在莲台坐，
来了一人间原因，
玄黄老祖开言道，
打坐炼磊修我身。
此人双膝跪在地，
乞请师父收门生。
玄黄睁眼将他看，
看他形格是神人，
此人后来有作为，
原是二磊化真身。
玄黄老祖开言问，
你今若无姓和名，
就取奇妙多好听。
奇妙子一听心欢喜，
手掌双合拜师尊。

玄黄忽把徒弟叫，
你我山上走一巡。
玄黄下了莲台座，
二人游玩看山林。
师徒玩游有几日，
看见一座石洞门，
石门框来石门坎，
就象金玉一般形。
师徒二人把门进，
举目观看喜十分。
