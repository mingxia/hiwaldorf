先天（2）

金鼓一住莫消停，
我把歌师尊一声，
请教先天啥情形？

歌师发问讲先天，
先天只有气一团，
黑里古洞漫无边。
有位老祖名黑暗，
大事小事她统管，
黑暗老祖无双亲，
无影无形无脸面，
伸手不能见五指，
怪里怪古掌江山。

唯有黑暗根基深，
歌师可能讲的清，
为何黑暗无父母？
黑暗过后混沌生？

提起黑暗这根基，
这个根古长的很。
那时没有天和地，
那时不分高和低，
那时没有日月星，
入和万物不见形，
汪洋大海水一片，
到处都是黑沉沉。
黑暗老祖无双亲，
万磊相聚化仙身。
不知多少年和代，
黑暗之后出混沌，
混沌之后见光明。

歌师提起混沌祖，
我将混沌问根古，
谁是混沌父？
谁是混沌母？
混沌出世啥时候？
还有什么在内头？
歌师对我讲清楚，
我拜歌师为师父。

当时有个幽汗祖，
幽汗生浦湜，
浦浞就是混沌父，
幽汗就是混沌母，
母子成婚配，
生下一元物，
包罗万象在里头，
好象小鸡未孵出。

混沌从前十六路，
汗清又出世，
一路生幽泉，
幽泉变滇汝，
二路生江泡，
三路生玄真，
四路生泥沽，
五路生汗水，
六路生提沸，
七路生幽泉，
八路生泗流，
九路生红雨，
十路生清气，
十一生洚沸，
十二生重汗，
十三生涅伍，
十四生泣涅，
十五生洞汰，
十六生江沽，
江沽他才造水土。

当时有个混沌祖，
天地自然有根古，
内中他还有一物，
名曰泡罗生水土。

赤气降了地，
泡罗吐清气，
生出小无提，
提子名沙泥，
沙泥传沙滇，
沙滇传沙沸，
沙沸传红雨，
红雨传化极，
化极传苗青，
苗青传石玉。

有个老母黑天坐，
神通广大无法说，
石龙老母是她号。
老母收下一门人，
复元就是他的名，
复元法术多妙哉，
