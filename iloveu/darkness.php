长出玄黄老祖来。
玄黄出世玄又玄，
无有日月共九天，
无山无水无星斗，
没有火来没有风，
更无人苗和万物。

玄黄出世玄又玄，
还得问问昆仑山。
且说昆仑山一座，
一道黄气往下落，
一道赤气空中起，
一道黑气往上升，
一道白气透九重，
一道紫气雾沉沉。
