四本吹到农户家，
唱起火炮花鼓歌；
五本吹到田间里，
挖田薅草锣鼓歌；
六本吹到孝堂内，
取名就叫丧鼓歌。
若果此歌未解错，
和和气气唱几个。

丧鼓歌本有名篇，
号称四游并八传，
我也多少知道点。

《东游记》里故事多，
王母娘娘并八仙；
《西游记》里妖怪多，
唐僧取经赴西天；
