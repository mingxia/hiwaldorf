歌引

梦里听见丧鼓响，
一个轱辘翻下床，
鞍起鞋子奔孝堂。

走进孝堂抬头望，
好多歌师早到场。
上面坐的是姜尚，
渭水钓鱼扶周王；
下面坐的是张良，
火烧栈道保刘皇；
左边坐的诸葛亮，
神机妙算世无双；
右边坐的徐茂公，
打开八卦知阴阳；
东边坐的包丞相，
辅佐仁宗掌朝纲；
西边坐的太平郎，
一付双锏镇瓦岗；
南边坐的岳家将，
牛头山上威名扬；
北边坐的吴学究，
水浒寨里数栋梁。

愚下今日交好运，
歌场得会众先生，
好似文王访贤臣。

鼓上行礼作个揖，
有请各位收徒弟。
愚下只因家贫困，
从小没进学堂门，
五经四书全不知，
十个数也认不真：
一字认成锄头把，
二字认成箩面架，
三字认成江踏坎，
四字认成火龙圈，
五字认成砂罐把，
六字认成草鞋扒，
七字认成杵路棍，
八字认成尖犁铧，
九字认成秤钩钩，
十字认成树叉叉。
今日歌场拜师尊，
好学一点歌学问。

歌师莫说谦虚话，
久闻您老名气大，
胳膊上长毛老行家。

今日鼓上来相会，
前世修行有缘法。
歌师好比一桌酒，
日日斟来月月有；
愚下好比一蔸葱，
