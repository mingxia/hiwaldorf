天地劫数还未满，
三番洪水泡天庭，
命中注定有磨难，
三番洪水三分天，
才有人世往下传。
洪水泡天有三番，
相继一万八千年，
故事多的讲不完。
没了日月没了星，
四处又是黑暗暗，
没了万物没了人，

五神六仙定会元。
一元分为十二会，
十二会满为一元，
若还十二会元满，
定是天地生死卷。
洪水泡天有三番，
水分三色不一般，
头番是黑水，
昊天走灵山；
二番是红水，
弘钧老祖现；
三番是清水，
末叶他为先。
三番洪水不一般，
水色三样有根源，
我在鼓上慢慢谈。
黑暗之时有阴山，
