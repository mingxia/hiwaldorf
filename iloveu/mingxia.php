吃的苦来说不尽。
老天不负有心人，
闻听水神名北溟，
北方有雪海，
雪海藏玄冰，
玄冰就是水母亲，
玄冰化开海洋存。
北溟老祖掌玄冰，
洞府藏在海中心，
海的中心叫黑谷，
黑谷幽深无比伦。

江沽闻言心中喜，
一路向北寻过去，
要找北溟解危急。
急急走来忙忙行，
吃苦受罪都不提，
