先天之黑暗混沌

唱歌的人胆子大，
火不烧山石不炸，
歌不盘本味道差。

丧鼓打到三更三，
《黑暗传》歌开了篇，
在下鼓上请圣贤，
黑暗之事起先天。
先天本在盘古前，
先天之时最黑暗。
黑暗咋方起？
黑暗咋方变？
什么老祖坐神坛？
怎么来把乾坤管？
麻烦歌师细指点，
根根芽芽慢慢谈。
金鼓一停我接腔，
学着来把先天讲，
不知说的象不象。
先天只有气一团，
不分青红和紫蓝，
黑里咕咚漫无边，
伸手不把五指见。
当时什么都没有，
只有一个老祖先。
老祖有名叫黑暗，
无影无形无脸面，
没有爹来没有妈，
更无亲戚和六眷，
黑暗老祖掌乾坤，
怪里怪古掌江山，
大事小事她统管，
不知过了多少年。

提起黑暗根基深，
