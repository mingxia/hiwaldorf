一道黄气往上飘，
一道赤气往上悬，
一道黑气往上升，
一道白气往上钻，
一道紫气腾雾烟，
五色滚成一个园。

五色瑞气结彩云，
一声响亮落地平，
一尊黄石现了形。
黄石高来有九丈，
腰园十丈还有零。
一人来到石上坐，
哈出气来石变形。
黄石变成莲花身，
莲台坐着一真人，
双目紧闭暗思忖，
要为自己取姓名。
两山之间有一孔，
内藏玄黄二宇文，
一个玄字借做姓，
一个黄字就为名。

玄黄莲台正修行，
台下忽然来了人，
双膝跪地叫师尊。
老祖睁眼把他看，
见他形格非凡品，
原是二气化真身，
后来作为大的很。
开口就把门生称，
拜我为师我答应，
赐你一个名和姓，
姓奇名妙最好听。
奇妙一听心欢喜，
双手合掌谢师尊。

玄黄莲台苦修行，
忽然一日动了心，
要到山上走一巡。
老祖人在前面走，
弟子奇妙随后跟，
游山玩水散精神，
寻到一处石洞门。
石门框来石门坎，
就象金玉一般形，
内有长开不谢花，
一步一处好风景。

师徒边走边赏景，
不觉来到三重门，
又高又大没有名。
老祖忙对弟子言，
此洞有诗说根源：
四域之内独生俺，
能知变化长生衍，
掌握皆归它发出，
能制天地玄机关。
你我洞内可安身，
立起石碑在门前，
碑高要有五丈三，
四丈带零是碑宽，
一门取名洪蒙洞，
二门取名波恩馆，
三门就叫云游宫，
一幅对联贴两边：
一粒粟里藏世界，
半口锅里煮坤乾。

洪蒙洞内好安静，
师徒修身又养性，
淡淡泊泊度日辰。
一日玄黄心烦闷，
想到洞外散精神，
叫声弟子你是听，
快快带路往前行。
出了洞门仔细看，
只见地眼放光明，
青赤二气团团转，
结成圆物囫囵形，
一声响亮落大地，
落在滑塘乱滚滚。
圆物打滚不打紧，
放出毫光怕煞人，
玄黄上前看分明，
知道此乃生天根，
忙叫奇妙滑塘行，
取来宝珍莫消停。
奇妙当下领了命，
飞步来到滑塘坑，
里里外外看分明。
只见一块大岩石，
高有百丈粗的很，

岩石下边一深渊，
名为五行滑塘坑，
圆物落在塘中间，
还在不住溜溜滚，
身个不小也不大，
腰圆三尺铃五分。
奇妙正腰取宝珍，
天空飞下一个人，
唱了一本难念经。
此人身长有五丈，
红脸黑须大眼睛，
四个獠牙颠倒挂，
眉如钢J1目似钉，
落在滑塘不说话，
伸手就要取宝珍。
此人就是浪荡子，
他是一气化真身。
奇妙见了吃一惊，
忙与浪荡评理性，
奉劝不要耍聪明。
圆物本是生天根，
若是捞坏一命倾。
浪荡听罢哈哈笑，
双手直向塘中伸，
捧出宝珍手上寸，
扬言就要一口吞。
奇妙子说你不敢，

浪荡子说我就行，
三个不字未出口，
宝珍已被浪荡吞。
奇妙一见怒生嗔，
拉住浪荡不松劲，
直往莲台见师尊。
二人来到莲台下，
奇妙就把实情禀，
说是宝珍被他吞，
就看师父咋施刑。
玄黄一见奇妙子，
大骂畜牲无礼性，
为何见我不下跪？
姓甚名谁说我听！
浪荡闻听玄黄问，
