后才慢慢道分明。
东海有个道法主，
荷叶老祖是他名，
我乃老祖一弟子，
大名浪荡四海行，
我岂向你来下跪，
惹怒老祖不饶人。
玄黄一听笑哈哈，
我把身份对你夸，
看你到底怕不怕。

气正万化我为先，
炼好万化出先天，
黑暗之中唯我贤，
玄黄法术大无边。
浪荡听罢笑几声，
吾神不信半毫分，
看你究竟逞啥能？
玄黄闻言动了真，
手挽剑柄要斩人。
剑诀一念喝声斩，
半空飞下剑一根，
口中连连骂畜牲，
快快还我贵宝珍，
若将宝珍还给我，
万事罢休不理论。
谁知浪荡不示弱，
