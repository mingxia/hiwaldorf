黎民将他把罪问，

他才让位给尧君。
说起尧帝有根痕，
他本生在丹陵城，
姓尹名祁有德行。
尧帝登基号高唐，
身占火德掌乾坤。
当时灾难连年生，
天道无常人遭瘟，
十个太阳同时出，
烈焰当空热难忍，
禾苗树木全晒死，
人人地穴去藏身，
又加狂风吹不停，
房屋顿时扫干净。
还有三个怪兽禽，
大鱼大蛇大猪精，
三怪同时来咬人，
百姓吓得掉了魂。
尧帝本是有道君，
便把后羿派上阵，
要叫妖怪一扫平。
后羿弓箭如天神，
奉命披挂忙出征，
首先便把三怪射，
，
斩尽杀绝不留情。
接着又与风伯斗，
马上风静天空明，

最后他才射日头，
累得浑身汗淋淋，
一箭一个太阳落，
两箭双日坠埃尘，
一连射出九支箭，
九个太阳不见形。
后羿射落九个日，
背上又把箭来取，
忽闻空中如雷鸣。
日光仙子发了话，
叫声后羿你是听，
如今妖星都除尽，
要留红日照万民。
后羿当时就跪拜，
拜谢日光太阳神，
从此阴阳才有序，
万物生长好乾坤。
尧帝赏了大功臣，
百姓也把后羿尊。
尧帝登基在甲辰，
癸未之年把驾崩，
在位七十二年整。
尧帝年高不糊涂，
要把龙位传能人，
他本有子名丹朱，
却是一个不肖生，
深怕丹珠碍事情，

远远放到房山陵。
遍天之下访贤能，
贤能接位才放心。
初把帝位让许由，
许由躲到箕山阴；
再把帝位让子交，
子交推说他有病。
让来让去无人接，
越思越想越烦闷。
尧帝当时好纳闷，
谁是江山继承人？
继续访来继续问。
一日他把厉山上，
见到有人正力耕，
问到天下大事情，
对答如流真精明。
此人有名叫大舜，
尧帝见了喜万分，
当下便把龙位让，
从此舜帝掌乾坤。
说起舜帝根痕深，
他是轩辕八代孙，
家谱之上记的明。
轩辕有子名昌意，
昌意又把颛顼生，
颛顼生穷蝉，
穷禅生敬康，

敬康生句望，
句望生峤牛，
峤牛生兆牛，
兆牛生瞽瞍，
瞽瞍取妻握登女，
姚墟之野生舜君。
大舜本是穷苦人，
尝遍人世艰和辛，
才能忠心为百姓。
三岁之上丧母亲，
没娘的孩子最伤心。
父亲又把后母娶，
后妈涂氏少德性。
涂氏生下一男儿，
象字便是他的名。
后妈视象如珍宝。
只把舜做外家人。
兄弟二人都长大，
后妈怕把家产分，
一心想把舜帝害，
几番下毒未得逞，
多亏舜帝福分大，
毒药尽被黄狗吞。
舜帝长到二十岁，
去到厉山把田耕，
苦扒苦熬度日辰。
有空就把雷泽下，

雷泽边上做渔人；
时常牧羊寅河边，
又制陶瓦在河滨。
尧帝知舜好品行，
当下将女许婚姻。
长女名字叫娥皇，
次女有名唤女英，
姐妹同做舜帝妻，
二人一同事夫君。
尧帝还把舜帝偿，
有牛有羊又仓廪。
舜帝回家见父母，
两个娇妻一路行，
后妈母子生妒心。
想把舜帝财产吞，
设下一计毒的很：
说动瞽瞍叫大舜，
仓廪里面办事情，
象弟随后悄悄跟，
外面把门偷锁紧，
忽然放起一把火，
烈火把舜困中心。
舜帝敲门无人应，
却见斗笠屋里存，
忙把斗笠戴头顶，
谁知斗笠能飞行，
飞身已到家门口，
毫毛没有少一根。

后妈母子计未逞，
心中又把毒计生，
瞽瞍真是下眼睛。
父亲要舜去淘井，
象弟随后推石滚，
要用石头把井填，
想叫舜帝命归阴。
哪知他家那口井，
却是狐精一后门，
九尾狐精早知晓，
早将小狐安排定。
舜帝刚把井底下，
便有狐精把路引，
径直回到卧房内，
漫拔琴弦好散心。
父母二人与象弟，
还在井边把地平，
忽闻房里琴声响，
走进一看掉了魂，
方知舜帝有神佑，
害舜之心从此停。
舜为天子号有虞，
不记象仇讲仁义，
后世帝王哪能及？
流放共工幽山地，
又放獾兜崇山里，
杀灭三苗于三危，

羽山之野平鲜于，
在位共是五十年，
然后皇位让大禹。
舜帝巡猎崩苍梧，
娥皇女英心中苦，
泪水灌满洞庭湖。
舜帝有子名商均，
商均生来少城府，
舜帝传位给禹王，
大禹治水传千古。
大禹也是贤明君，
五帝最后一个人，
为民治水费辛勤。
大禹治水有根痕，
父为水官名叫鲧，
当年奉了舜帝令，
要将水患一扫平。
他说治水很容易，
洪水来了土淹平。
上天偷下息壤来，
息壤筑得河堤成。
息壤本是一宝珍，
果然神奇无比伦，
洪水涨来堤也升，
河底升到半天云，
有朝一日决了口，
千家万户把命倾。

舜帝一见怒生嗔，
以土淹水事不成，
水官获罪不轻饶，
斩首便在羽山顶。
又闻鲧有子叫禹，
该为父亲赎罪行。
当下便把禹王传，
命他治水莫稍停。
禹王治水办法新，
先将九河来疏浚，
一十三年费辛勤。
疏汝沂，排淮泗，
导江河，通漯济，
诸水都向大海流，
水患从此断了根。
禹王治水十年整，
三过家门都没进。
他把许多规矩定，
时时处处讲准绳：
干活如同老百姓，

不怕日晒雨又淋；
吃饭从来不讲究，
野菜也能囫囵吞。
他的心肠真个好，
路见罪犯也不忍，
下车细把原因问，
查有冤情就改正。
舜帝一见心中喜，
当下选做接班人。
禹王接位掌乾坤，
二十七年世太平。
晚年他将诸候巡，
天星陨落会稽城，
至今江山胜迹在，
千秋万代颂英灵。
