肚里无货难搭言，
豆渣不能贴门板，
只能跟着胡呀哒，
有请各位多包涵。

久闻歌师学问高，
天上地下无不晓，
愚下鼓上来请教。

歌是前朝何人作？
何年何月才出歌？
什么人他写歌本？
什么人传下什么歌？
什么先生肩挑着，
打从什么湖上过？
什么小姐想唱歌，
扯的歌本掉下河？
什么山上晒歌本？
什么风吹的满山坡？
歌本吹到何处去？
各自取名叫什么？
有请先生道明白，
鼓上拜你为大哥。

桑木扬叉柳木拐，
鸭子下河把尾摆，
先生唱歌学生解。

歌是前朝张郎作，
周王登基才有歌。
比干丞相写歌本，
孔子传下六本歌。
董仲舒他肩挑着，
打从洞庭湖上过，
龙王小姐想唱歌，
扯落歌本掉下河，
须弥山上晒歌本，
狂风吹的满山坡。
一本吹到天空去，
牛郎织女唱情歌；
二本吹下东海洋，
渔民拣到唱渔歌；
三本吹到庙堂里，
