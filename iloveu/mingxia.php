三皇五帝

洪水泡天磨难尽，
天清地明水也清，
三皇五帝治乾坤。
这里再把歌师请，
天皇之事可晓明？
天皇出世人多少？
天皇弟兄共几人？
怎么来把天下治？
一共过了多少春？
金鼓一住莫消停，
