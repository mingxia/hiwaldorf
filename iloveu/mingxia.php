他的师父是哪个？
什么本事大的很？
玄黄出世啥时候？
出世时候啥光景？
烦请歌师道清楚，
愚下洗耳来恭听。

歌师问起玄黄根，
玄黄根子深的很，
四句诗文好证明：
一块黄石九丈高，
四四方方埋仙苗，
老祖坐在石台上，
霞光直闪透云霄。

说起玄黄有根苗，
他有师父黑天坐，
名叫石龙老婆婆。
石龙老母黑天转，
来到一座奇山前，
此山象是笔架形，
下为黄来上为玄，
站在山顶往远看，
两座神山首尾连，
一座就是昆仑山，
一座就是青龙山，
忽见两山合拢来，
合拢只在眨眼问，
昆仑山上冒青烟，
青烟直往笔架旋，
青龙山上冒黄烟，
黄烟也往笔架旋，
两烟相连化一胎，
现身就在笔架尖，
老母早知其中情，
收为门徒唤复元，
修行千载出了道，
才把乾坤来接管。

玄黄出世玄又玄，
没有日月没有天，
没有山来没有川。
风风火火都没有，
不见万物人苗面，
混沌世界昏昏暗，
谁人见了心都烦。

玄黄出世玄又玄，
