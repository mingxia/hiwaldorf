九路生红雨，
十路生清气，
十一生洚沸，
十二生重汗，
十三生里伍，
十四生丘里，
十五生洞汰，
十六生江沽。
当时乾坤啥没有，
江沽他才造水土。

歌师说起江沽皇，
江沽出世有名堂，
