## 后天（开天辟地）

歌师果然好本领，
先天之事记的真，
再问后天啥情形。
为何先天尽黑暗？
为何后天出光明？
盘古他是何方人？
怎样出世怎样生？
怎样才把天地分？
怎样才把日月请？

紧打鼓，慢逍遥，
黑暗根源从头道，
不知说的好不好。
青龙山，昆仑山，
两山都有万丈高，
两山相对感阴阳，
两水相连乐陶陶，
青气其中含，
赤气其中包，
相混成圆物，
奇妙又奇妙。
当日玄黄斩浪荡，
尸分五处血流淌，
昆仑山才出海洋。
一山长成五龙样，
天精地灵里头藏，
阴阳五行始聚化，
盘古怀在它中央，
过了一万八千载，
世上才出盘古皇。

自从昆仑它长成，
山上就有五条岭，
五条岭呈五龙形。
五龙口中吐血水，
弯弯曲曲多古怪，
血水聚在深潭内，
深潭里面结仙胎，
盘古从此来孕育，
长了一万八千载。

说起盘古有根痕，
当时乾坤未成形，
一片黑暗与混沌。
青赤二气不分明，
迷迷蒙蒙几千层。
不知过了多少春，
二气相交产万灵，
盘古怀在混沌内，
金木水火是他父，
土是盘古他母亲。

说起盘古来出生，
第一功劳数弘钧，
铁笔将他画人形。
铁笔本是玄黄赠，
画天画地又画人，
弘钧老祖拿在手，
左右不停汗泠泠。
一画二画来出世，
三画四画出顶门，
五画六画眉毛生，
七画八画七窍明，
九画十画画四肢，
十一十二画眼睛，
十三十四画骨节，
三百六十零五根，
十五十六画汗毛，
十万八千有余零。
画得顶平额角起，
画得肝肺手连心，
画得元气满盈盈，
画得五行各安身，
崩婆借象现埃尘，
这是后话当别论。

盘古出世多古怪，
引出四句诗文来，
未来之事早明白。
盘古出世雷声响，
一股灵气透天光，
冲开黑暗云和雾，
小小微亮在西方。
盘古昏昏如梦醒，
伸腿伸腰出地心，
麻烦事情多的很。
睁开眼睛抬头看，
天为锅来地为盆，
既无缝来又无门，
严丝严缝扣的紧，
想站起身把腰挺，
一头碰的脑壳疼，
用头顶，顶不开；
用脚蹬，蹬不成，
手摸脑壳心暗想，
要用办法分乾坤。

盘古心中好纳闷，
一路奔波一路行，
咋方才把天地分？
往东方，东不明；
往南方，看不清；
往北方，雾沉沉；
往西方，有颗星。
盘古摘来星星看，
原是西方庚辛金。
金星当时来变化，
变成一把斧头形，
不象金来不象银，
不是铁匠来打成，
拿在手上千斤重，
舞在空中万点星。

盘古得斧喜在心，
继续赶路往前寻，
果然又见一宝珍。
一把錾子面前存，
遍体溜光放光明，
不是金来不是银，
不是铁匠来打成，
此乃上古一珍宝，
敲天坎是它的名，
想它大来它就大，
叫它小来不见形，
想它重来它就重，
叫它轻来它就轻。

盘古将斧扛在肩，
錾子藏在怀里面，
高高兴兴把路赶。
只见一山把路拦，
身子圆来顶顶尖，
直上云端杵九天，
银光四射耀人眼。
此也上古一宝珍，
不是金来不是银，
不是铁匠来打成，
钻地钎是它的名，
想它长来它就长，
叫它短来不见形。

盘古得了宝三件，
要几喜欢几喜欢，
当下就把大事干。
又是斧头砍，
又是錾子钻，
钎子戳的直冒眼，
浑身上下流大汗。
东边砍，西边砍，
往上戳，往下钻，
一斧砍断混沌根，
只见青气往上旋，
化做玉皇殿，
整整九重天；
一錾钻开混沌门，
只见赤气往下灌，
化做地狱府，
一十八层满。

原来混沌和黑暗，
青赤二气搅成团，
相分才出地和天。
盘古站在天地间，
脚蹬地来头顶天，
身子长一尺，
天便高一丈，
盘古长了一丈八，
才有高天十八盘：
身子长一尺，
地便厚一丈，
盘古长了一丈八，
才有地狱十八变。

盘古分开天和地，
还是天黑地不明，
寒风嗖嗖彻骨冷。
日月何时上天庭？
咋方又出满天星？
咋方又有风云会？
咋方又有雨淋淋？
盘古后来哪去了？
是死是活是为神？
三山五岳哪里来？
人苗初在那哪生？
歌师自来有学问，
还请一一道分明。

歌师莫急请消停，
让我慢慢讲来听，
不周不到再问高明。
盘古分了天和地，
天地依然是混沌，
盘古心里好烦闷，
便找日月二星君。
一路赶，急急行，
只见东方有座山，
山上似有毫光隐，
欲往山上探真情，
磕磕绊绊路难行。
盘古举斧便乱砍，
找到两个大洞门。
一个名叫太阳洞，
洞中住着太阳神，
洞中有棵扶桑树，
太阳树上安其身；
一个名叫太阴洞，
洞中住着月亮神，
洞中有个咸池海，
月君海边安其身。
盘古见了二神面，
连忙上前把礼行，
他说天地两相分，
就缺光明照乾坤，
我今前来无别因，
相请二位升天庭。
盘古说话动真情，
二神满口就答应，
太阳太阴上了天，
从此天下见光明。
太阳太阴儿孙多，
从此才有满天星。

闻听盘古请日月，
还有故事妙的很，
我来说给各位听。
先天时候有一神，
皮罗崩婆是她名，
当日奉了弘钧令，
日月洞中隐其形，
而今闻听盘古到，
借象还魂又现身。
她为盘古指路程，
又把真情细告禀：
日神和月君，
夫妻两个人，
月姓唐来日姓孙，
唐末孙开是大名，
夫妻恩爱感情深，
从来不愿两离分。

闻听盘古请日月，
我也插上一段情，
不知说的真不真。
盘古开出天和地，
准备后山去修行，
忽听前面师父叫，
佛祖开言把话论：
差你咸池走一称，
相请日月上天庭。
盘古听了心纳闷，
心想道浅根不深，
如果日月请不动，
咋有脸面回师尊。
无奈还是往前行，
寻到咸池好风景。
咸池原是大海洋，
宽有九万带余零，
深有万丈不见底，
日宫月殿里面存。
盘古进洞说来意，
真叫二神难为情。
他们本是好夫妻，
海中金子配水精，
恩恩爱爱度日辰，
阴阳媾和难离分。
直言便对盘古讲，
不好从命有原因。
盘古无奈转回程，
莲台之前禀师尊，
师尊授他法与咒，
叫他再往咸池请。
盘古二进咸池门，
二神不理也不听，
顿施妙法念咒语，
念出一个七字经，
“暗夕姐多拔达罗”，
咒的日月心大惊，
当即别了咸池府，
跟随盘古上天庭，
夫妻一月会一面，
才有日落和月升。

日月星君上天庭，
满天星斗众子孙，
乾坤之内才光明。
若问周天有多大？
三百六十五度整。
若问极地有多深？
一百八十有余零。
玄黄化无极，
无极生太极，
太极生两仪，
两仪生四象，
四象生八卦，
八卦生阴阳，
阴阳有序万物生。

日月星君上天庭，
盘古从此隐了形，
浑身配与天地灵。
身配五岳山高峻，
目配日月天更明，
血配江河滚滚流，
毫毛配与草木盛。
头在东岳泰山顶，
脚在西岳华山岭，
左臂南岳衡山树，
右臂北岳恒山林，
肚子恰在正中央，
挺起嵩山半天云。
肌肉入田土，
骨头金石成，
精髓为珠玉，
汗流变雨淋，
诸虫随风化，
才有众黎民。

日月星君上天庭，
盘古从此隐了形，
还有一段好经文。
盘古身化五岳岭，
又配五方分五行，
头合东方甲乙木，
脚配西方庚辛金，
面合南方丙丁火，
背对北方壬癸水，
身配中央戊己土，
天地和乐万物生。
